#include <string.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;
	size_t			i;

	i = 0;
	str = (unsigned char *)s;
	while (i < n && str[i] != (unsigned char)c)
		i++;
	if (i < n && str[i] == (unsigned char)c)
		return (&str[i]);
	return (NULL);
}
